void main() {
  int a = 5;
  int b = 10;

  int perkalian = a * b;
  double pembagian = a / b; // menggunakan double untuk menghindari pembulatan integer
  int penjumlahan = a + b;
  int pengurangan = a - b;

  print('a * b = $perkalian');
  print('a / b = $pembagian');
  print('a + b = $penjumlahan');  
  print('a - b = $pengurangan');
  
}
